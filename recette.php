<h1>Bûche de Noël Chocolat - Poires </h1>
<h2 class="item-header" data-folded="►" data-extended="▼">Ingrédients (pour 8 personnes)</h2>
<div class="h2content item-content">
<p class="noind item-header" data-folded="+" data-extended="–">Pour le biscuit :</p>
<ul class="item-content">
<li>4 œufs
<li>115 g de sucre
<li>120 g de farine
<li>25 g d'amandes effilées
</ul>
<p class="noind item-header" data-folded="+" data-extended="–">Pour la mousse chocolat :</p>
<ul class="item-content">
<li> 4 œufs
<li> 125 g de chocolat noir pâtissier
<li> 10 cl de crème fraîche (15 à 20%)
</ul>
<p class="noind item-header" data-folded="+" data-extended="–">Pour les poires :</p>
<ul class="item-content">
<li>3 poires de taille moyenne (William/Barlett)</li>
</ul>
<p class="noind item-header" data-folded="+" data-extended="–">Pour la couverture :</p>
<ul class="item-content">
<li>100 g de chocolat noir pâtissier
<li>8 à 12 cl de crème fraîche
</ul>
</div>

<h1>Recette</h1>
<h2 class="item-header" data-folded="+" data-extended="–">Mousse au chocolat</h2>
<div class="h2content item-content">
<p>Séparer les jaunes des blancs d'œuf et monter ces derniers en neige ferme
(5 min au batteur électrique), avec une pincée de sel.</p>
<p>
Casser le chocolat en morceaux, pour le faire fondre dans un bol au
micro-ondes (1 à 2 min puissance max, en remuant 2 ou 3 fois).
Ajouter les jaunes d'œuf, en remuant vivement avec une fourchette (laisser
tiédir un peu le chocolat fondu s'il est trop chaud, afin d'éviter de cuire
les jaunes), puis la crème fraîche et mélanger jusqu'à obtention d'une ganache
onctueuse.</p>
<p>
Prélever 1/3 des blancs d'œufs et les incorporer délicatement à la préparation
chocolatée, en remuant doucement avec une spatule en bois (ne pas « écraser »
les blancs d'œufs, il faut être patient et les laisser se « dissoudre » dans le
chocolat !).</p>
<p>
Transvaser ensuite dans le restant de blancs d'œufs et mélanger jusqu'à
homogénéité, en essayant d'incorporer autant d'air que possible à chaque tour
de cuillère. Couvrir d'un film plastic alimentaire et réserver au frigo.</p>
</div>

<h2 class="item-header" data-folded="+" data-extended="–">Biscuit</h2>
<div class="h2content item-content">
<p>Séparer les jaunes des blancs d'œuf et monter ces derniers en neige ferme
(5 min au batteur électrique) avec une pincée de sel.</p>
<p>
Blanchir les jaunes avec le sucre, puis incorporer la farine en pluie jusqu'a
obtention d'une pâte assez compacte.</p>
<p>
Ajouter les blancs d'œufs par petites quantités, en remuant avec consistance;
la pate s'aère et prend peu à peu une consistance crémeuse.</p>
<p>
Préchauffer le four à 180°C (Th 6).</p>
<p>
Aligner une plaque de four de 30×40cm (2cm d’épaisseur ou plus) avec du papier
sulfurisé.</p>
<p>
Répartir les amandes effilées et verser la préparation du biscuit.
Enfourner et laisser cuire 10-12 min. De légères bulles doivent se former à la
surface, indication que le biscuit est cuit; ne pas laisser brunir !
Laisser refroidir le biscuit sur la plaque.</p>
</div>

<h2 class="item-header" data-folded="+" data-extended="–">Montage de la bûche</h2>
<div class="h2content item-content">
<p>
Chemiser un moule à cake avec du film plastique, en prévoyant 10 cm de plus
sur chaque côté. Sortir la mousse au chocolat du frigo.
Peler et écorer les poires, puis les couper en fines lamelles.</p>
<p>
A l’aide d’un couteau, découper 3 rectangles de biscuits de la taille du moule
à cake (conserver les chutes, elles serviront à la décoration !). Placer le
premier rectangle au fond, les amandes face à vous.</p>
<p>
Etaler 2 grosses cuilllères de mousse au chocolat sur le biscuit et disposer 2
rangées de lamelles de poires, bien serrées les unes aux autres.
Recouvrir de mousse au chocolat (même quantité) et poser le deuxième rectangle
de biscuit, en pressant légèrement pour compacter l’étage inferieur.</p>
<p>
Répéter l’opération pour le deuxième étage et finir avec le 3ème rectangle de
biscuit.</p>
<p>
Rabattre les bords du film plastique et placer au frigo pour la nuit (ou au
minimum 3 h).</p>
</div>

<h2 class="item-header" data-folded="+" data-extended="–">Glaçage et décoration</h2>
<div class="h2content item-content">
<p>
Pour le glaçage, casser le chocolat en morceaux et le faire fondre dans un bol
au micro-ondes; laisser tiédir avant d’ajouter la crème fraîche.</p>
<p>
Démouler la bûche sur un plat de service. A l’aide d’une spatule ou d’un
couteau large, recouvrir la bûche d’une couche de glaçage, puis strier la
surface avec une fourchette.</p>
<p>
Reste à décorer le gâteau ! Et là, laissez libre cours à votre imagination :
champignons meringués, sucre glace, noix et fruits secs (éventuellement
enrobées de chocolat noir ou blanc), pétales de roses en chocolat, sujets en
massepain...</p>
<p>
Les restants de biscuit peuvent aussi être utilisés pour former une
maisonnette (recouverte de glaçage pour la maintenir en place); les
possibilités sont infinies !</p>
<p>
Réserver la bûche finie au frigo jusqu'au dernier moment.</p>
</div>
